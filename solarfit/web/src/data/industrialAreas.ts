function box(lon: number, lat: number, width = 0.055, height = 0.04): [number, number][] {
  const west = lon - width / 2;
  const east = lon + width / 2;
  const south = lat - height / 2;
  const north = lat + height / 2;
  return [
    [west, south],
    [east, south],
    [east, north],
    [west, north],
  ];
}

